void Command_parser()
{

	if(temp == '#')
	{

		index_command = (index_command == 0)? 28: index_command - 2;

		for(int i = 3; i >= 0 ; i--)
		{

			int k =0;

			duty[i] = 0;

			while(k < 3)
			{

				if(command[index_command] > '9' || command[index_command] < '0')
				{
					return;
				}

				duty[i] = duty[i] + (int) pow(10, (double)k)*(command[index_command] - '0');

				index_command =  (index_command == 0)? 29: index_command - 1;

				k++;

			}


			if(duty[i] > 200)
			{
				return;
			}

			if(duty[i] >= 100)
			{
				dir[i] = 1;

				duty[i] -= 100;
			}

			else dir[i] = 0;


			if(command[index_command] != ':')
			{
				return;
			}

			index_command = (index_command == 0)? 29: index_command - 1;

		}


		for(int i = 3; i <= 0; i--)
		{
			if(check_command[i] != command[index_command])
			{
				return;
			}

		}

		update_motor_state = 1;
	}

}





void Spin_M1(int speed, uint8_t dir)
{
	if(speed > 100) return;

	if(dir) GPIOD->ODR |= (1<<1);

	else GPIOD->ODR &= ~(1<<1);

	TIM2->CCR2 = speed;
}




void Spin_M2(int speed, uint8_t dir)
{
	if(speed > 100) return;

	if(dir) GPIOD->ODR |= (1<<3);

	else GPIOD->ODR &= ~(1<<3);

	TIM3->CCR2 = speed;
}



void Spin_M3(int speed, uint8_t dir)
{
	if(speed > 100) return;

	if(dir) GPIOD->ODR |= (1<<5);

	else GPIOD->ODR &= ~(1<<5);

	TIM4->CCR2 = speed;
}




void Spin_M4(int speed, uint8_t dir)
{
	if(speed > 100) return;

	if(dir) GPIOD->ODR |= (1<<7);

	else GPIOD->ODR &= ~(1<<7);

	TIM4->CCR4 = speed;
}


void Driver()
{
	if(update_motor_state)
	{
		update_motor_state = 0;

		Spin_M1(duty[0],dir[0]);

		Spin_M2(duty[1],dir[1]);

		Spin_M3(duty[2],dir[2]);

		Spin_M4(duty[3],dir[3]);
	}
}



