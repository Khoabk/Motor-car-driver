void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	switch(GPIO_Pin)
	{

		case GPIO_PIN_0:
		{
			counter[0]++;
			break;
		}
		case GPIO_PIN_2:
		{
			counter[1]++;
			break;
		}
		case GPIO_PIN_4:
		{
			counter[2]++;
			break;
		}
		case GPIO_PIN_6:
		{
			counter[3]++;
			break;
		}
		default:
			break;

	}

}



